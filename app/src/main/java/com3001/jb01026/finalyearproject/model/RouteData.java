package com3001.jb01026.finalyearproject.model;

public class RouteData {
    private int time;
    private int distance;

    public RouteData(int time, int distance) {
        this.time = time;
        this.distance = distance;
    }

    public int getTime() {
        return time;
    }

    public int getDistance() {
        return distance;
    }
}
